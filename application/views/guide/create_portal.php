				<h2>建立 portal <small>覺得這裡值得是個據點嗎？現在馬上上傳！</small></h2>


				<h3>哪些地方可以當 portal ？</h3>
				<p>
					官方有提供 <a href="https://support.google.com/ingress/answer/3066197?&ref_topic=2799270" target="_blank">Portal 審查規範與建議</a>，
					目前一般以廟宇、路邊藝術品（如溜滑梯、盪鞦韆、壁畫、各類石碑、台電變電箱等）最容易過關，

					另外需要特別注意若兩個 portal 距離太相近也可能被拒絕。

					官方的審查標準目前並非十分明確，也有些 portal 重複 submit 後還是過了。
					建議只要認為可以當成 portal 的就 submit 等待官方的審查，若失敗了也不用氣餒，可以再多試幾次。
				</p>

